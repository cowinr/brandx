"""Application defaults — the single source of truth for all brand keys.

Every brand key has a default value, a one-line comment (used as the comment in the
YAML written by `brandx init`), and a purpose string (used in the config-reference
doc generated by `brandx docsgen`).

The structure mirrors the YAML shape: top-level scalar keys and a nested `colours`
mapping. Nested mappings are represented as plain dicts; the resolver deep-merges them
so a single override leaves the rest of the palette intact.

Usage:
    from brandx.config.defaults import DEFAULTS
    val = DEFAULTS["identity"]["name"]["value"]
"""

from typing import Any


def _key(value: Any, comment: str, purpose: str) -> dict:
    return {"value": value, "comment": comment, "purpose": purpose}


