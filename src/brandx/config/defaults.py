# ---------------------------------------------------------------------------
# Root DEFAULTS mapping
# ---------------------------------------------------------------------------
# Nested sub-mappings (colours, fonts, date, identity) keep the same dot-separated
# override grammar as the flat keys. The resolver deep-merges nested dicts so that
# a partial palette override leaves the rest intact.

DEFAULTS: dict = {
    "identity": _IDENTITY,
    "colours": _COLOURS,
    "fonts": _FONTS,
    "date": _DATE,
}


def flat_defaults() -> dict[str, Any]:
    """Return a flat {key: value} dict of all defaults (nested keys dot-joined).

    Used by the resolver to seed the base config layer.
    """
    result: dict[str, Any] = {}

    def _walk(node: dict, prefix: str) -> None:
        for k, v in node.items():
            path = f"{prefix}.{k}" if prefix else k
            if isinstance(v, dict) and "value" in v and "comment" in v:
                result[path] = v["value"]
            elif isinstance(v, dict):
                _walk(v, path)

    _walk(DEFAULTS, "")
    return result


def nested_defaults() -> dict:
    """Return a nested {key: value} dict of all defaults.

    The shape mirrors the YAML a user would write: nested mappings for
    colours, fonts, date, and identity, with plain values (not the
    comment/purpose dicts).
    """

    def _extract(node: dict) -> Any:
        if isinstance(node, dict) and "value" in node and "comment" in node:
            return node["value"]
        if isinstance(node, dict):
            return {k: _extract(v) for k, v in node.items()}
        return node

    return {k: _extract(v) for k, v in DEFAULTS.items()}
